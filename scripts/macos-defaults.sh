echo "â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•"
echo ""
success "macOS defaults have been set!"
echo ""
warn "Some changes require a logout/restart to take effect"
echo ""
