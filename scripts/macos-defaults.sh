#!/usr/bin/env bash
# =============================================================================
# macOS Defaults Script
# =============================================================================
# Sets macOS system preferences programmatically
# Inspired by: https://github.com/mathiasbynens/dotfiles/blob/main/.macos

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Helper functions
